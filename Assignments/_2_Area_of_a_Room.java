// 2) Write a java program to find the area of a room.

public class _2_Area_of_a_Room {
    public static void main(String args[]) {
        double h = 5;
        System.out.println("The height of room is: " + h);
        double w = 10;
        System.out.println("The width of room is: " + w);
        double area = h*w;
        System.out.println("The area of the room is: "+ area);      
    }
}


// OUTPUT

// The height of room is: 5.0
// The width of room is: 10.0   
// The area of the room is: 50.0