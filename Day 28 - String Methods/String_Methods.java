public class String_Methods {
    public static void main(String args[]) {
        String line = "ProgrammingAadii";
        System.out.println("\nString is: " + line);
        System.out.println("\nLength of String is: "+ line.length());
        System.out.println("\nString after to Lower case is: "+ line.toLowerCase());
        System.out.println("\nString after to Upper case is: "+ line.toUpperCase());
        System.out.println("\nCharacter at 4th index is: " + line.charAt(4));
    }

}
