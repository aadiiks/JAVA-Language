// A program to understand Variables and Datatypes.

public class Var_Datatypes{
    public static void main(String args[]){
        int num = 10;
        System.out.println(num);
    }
}

// Here 10 is a value of type integer(int) that is needed to be stored in a container to get a specific address for accessing. So the container used is known as variable.
// = is the assignment operator. It tells what value is to be stored in which variable.
// num is the container known as variable in programming language which will store the value in itself.
// int is a datatype that tells the compiler that what type of value will be stored in the variable declared i.e. inside num

// When variables are declared inside a java class it's known as Attributes.
// When functions are declared inside a java class it's known as Methods.