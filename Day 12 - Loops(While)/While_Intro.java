// WAP to print Hello 10 times.

public class While_Intro {
    public static void main(String args[]){
        int counter = 1;
        while (counter <= 10){
            System.out.println("Hello");
            counter++;
        }
    }
}


// while loop is of type which just have condition as an argument. It checks for initialized value and compares to the condition given.
// It executes the loop if condition returns true else breaks if condition returns false.